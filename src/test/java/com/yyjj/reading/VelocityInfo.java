package com.yyjj.reading;

public class VelocityInfo {
	private  String packageName;
	
	private String model;
	private String modelPacage;
	private String module;
	private String lowerModel;
	
	public String getLowerModel() {
		return lowerModel;
	}

	public void setLowerModel(String lowerModel) {
		this.lowerModel = lowerModel;
	}

	public String getModelPacage() {
		return modelPacage;
	}

	public void setModelPacage(String modelPacage) {
		this.modelPacage = modelPacage;
	}

	public String getPackageName() {
		return packageName;
	}

	public void setPackageName(String packageName) {
		this.packageName = packageName;
	}

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public String getModule() {
		return module;
	}

	public void setModule(String module) {
		this.module = module;
	}
	
}
